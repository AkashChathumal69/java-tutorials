public class Student 
{
    private int id;
    private String name;
    private String course;
    
    public void setID(int id)
    {
        this.id=id;
    }
    public int getID()
    {
        return id;
    }
    public void setName(String name)
    {
        this.name=name;
    }
    public String getName()
    {
        return name;
    }
    public void setCourse(String course)
    {
        this.course=course;
    }
    public String getCourse()
    {
        return course;
    }
}
